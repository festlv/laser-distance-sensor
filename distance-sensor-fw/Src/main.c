static void LOG_MSG(const char* msg)
{
    uint8_t buf[256];
    buf[0] = '#';
    strncpy(buf + 1, msg, sizeof(buf) - 3);
    buf[sizeof(buf) - 1] = '\0';
    int msg_len = strlen(buf);
    buf[msg_len] = '\n';
    msg_len++;
    buf[msg_len] = '\0';
    CDC_Transmit_FS(buf, msg_len);
}
